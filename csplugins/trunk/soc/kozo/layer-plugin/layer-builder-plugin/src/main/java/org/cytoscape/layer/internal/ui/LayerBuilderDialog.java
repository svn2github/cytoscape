	private void generateIntegratedNetworkButtonActionPerformed(
			java.awt.event.ActionEvent evt) {// GEN-FIRST:event_GenerateIntegratedNetworkButtonActionPerformed

		List<CyNetwork> layers = new ArrayList<CyNetwork>();
		List<CyNetwork> connectors = new ArrayList<CyNetwork>();

		for (int i = 0; i < layeredNetworkListModel.getSize(); i++) {
			CyNetwork cyNetwork = manager.getNetwork((Long) layeredNetworkListModel.getElementAt(i));
			
			for (CyNode cyNode : cyNetwork.getNodeList()) {

				System.out.println(Integer.toString(i));
				cyNode.attrs().set(LAYER_NUMBER, Integer.toString(i));
				System.out.println(cyNode.attrs().get(LAYER_NUMBER, String.class));
				
			}
			
			layers.add(cyNetwork);
		}

		for (int i = 0; i < layerConnectionTableModel.getRowCount(); i++) {
			connectors.add(manager.getNetwork(layerConnectionTableModel
					.getValueAt(i, CONNECTOR)));
			System.out.println(layerConnectionTableModel.getValueAt(i,
					CONNECTOR));
		}

		// Setup
		builder.setSourceNetworks(layers, connectors);
		manager.addNetwork(builder.buildLayeredNetwork());

		this.dispose();

	}// GEN-LAST:event_GenerateIntegratedNetworkButtonActionPerformed

